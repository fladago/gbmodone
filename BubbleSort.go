package main

func BubbleSort(arr []int) []int {
	swapper := true
	for swapper {
		swapper = false
		for i := 0; i < len(arr)-1; i++ {
			if arr[i+1] < arr[i] {
				arr[i+1], arr[i] = arr[i], arr[i+1]
				swapper = true
			}
		}
	}
	return arr
}

